package yogesh.lecture.fristjava_program;

import java.util.Scanner;

public class Inputs {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        System.out.print("Enter Your Name  : ");
         String name = input.nextLine();//input.nextline() will take only an whole line of string
         System.out.print("Enter Your Roll No: ");
        int rollno = input.nextInt();
        System.out.println("Hi , " + name + " Your Roll no is :  " + rollno);

        int a = 234_000_000;// underscore are ignored
        System.out.println(a);
        String dog = input.next(); // it will print only frist word only
        System.out.println(dog);

        float marks = input.nextFloat();
        System.out.println(marks);
    }
}
